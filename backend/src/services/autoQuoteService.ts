/**
 * AI Auto-Quote Service — Instant Rate Quotes for Shipper RFQs
 *
 * Generates instant rate quotes using market data + historical lane pricing.
 * No human needed for standard lanes. Combines historical load data with
 * base rate calculations, seasonal adjustments, and SRL margin markup.
 *
 * KEY FEATURES:
 * - Historical lane pricing from last 90 days
 * - Equipment-specific base rates with fuel surcharges
 * - Hazmat, temp-controlled, weight, season, distance adjustments
 * - Confidence scoring based on historical data volume
 * - 24-hour quote validity with unique tracking IDs
 */

// ─── Interfaces ─────────────────────────────────────────────────────

export interface QuoteRequest {
  originCity: string;
  originState: string;
  destCity: string;
  destState: string;
  equipmentType: string;
  weight?: number;
  pickupDate?: string;
  commodity?: string;
  isHazmat?: boolean;
  isTempControlled?: boolean;
  customerId?: string;
}

export interface QuoteResponse {
  quotedRate: number;
  ratePerMile: number;
  estimatedMiles: number;
  confidence: "HIGH" | "MEDIUM" | "LOW";
  marketRate: { low: number; avg: number; high: number } | null;
  margin: number;
  breakdown: {
    linehaul: number;
    fuelSurcharge: number;
    accessorials: number;
    total: number;
  };
  validUntil: string;
  quoteId: string;
  autoGenerated: boolean;
  factors: string[];
}

