// ─── Distance Estimation ────────────────────────────────────────────

function getStatePairKey(stateA: string, stateB: string): string {
  const a = stateA.toUpperCase();
  const b = stateB.toUpperCase();
  return a < b ? `${a}-${b}` : `${b}-${a}`;
}

/**
 * Haversine formula for straight-line distance between two lat/lng points.
 * Returns miles. We apply a 1.3x road factor to approximate driving distance.
 */
function haversineDistance(
  lat1: number, lng1: number,
  lat2: number, lng2: number,
): number {
  const R = 3959; // Earth radius in miles
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

function estimateDistance(originState: string, destState: string): number {
  const origin = originState.toUpperCase();
  const dest = destState.toUpperCase();

  if (origin === dest) {
    return 250; // Intra-state default
  }

  // Check lookup table
  const key = getStatePairKey(origin, dest);
  if (STATE_PAIR_DISTANCES[key]) {
    return STATE_PAIR_DISTANCES[key];
  }

  // Fallback: straight-line with road factor
  const originCoord = STATE_CENTROIDS[origin];
  const destCoord = STATE_CENTROIDS[dest];
  if (originCoord && destCoord) {
    const straightLine = haversineDistance(
      originCoord.lat, originCoord.lng,
      destCoord.lat, destCoord.lng,
    );
    return Math.round(straightLine * 1.3); // 1.3x road factor
  }

  return 500; // Ultimate fallback
}

// ─── Market Benchmark ───────────────────────────────────────────────

export async function getMarketBenchmark(
  originState: string,
  destState: string,
  equipmentType: string,
): Promise<{ low: number; avg: number; high: number; count: number } | null> {
  const ninetyDaysAgo = new Date();
  ninetyDaysAgo.setDate(ninetyDaysAgo.getDate() - 90);

  const loads = await prisma.load.findMany({
    where: {
      originState: originState.toUpperCase(),
      destState: destState.toUpperCase(),
      equipmentType,
      pickupDate: { gte: ninetyDaysAgo },
      status: { in: ["DELIVERED", "COMPLETED", "IN_TRANSIT", "AT_PICKUP", "AT_DELIVERY"] },
    },
    select: {
      rate: true,
      distance: true,
    },
  });

  if (loads.length === 0) return null;

  const rates = loads.map((l) => l.rate).sort((a, b) => a - b);
  const sum = rates.reduce((acc, r) => acc + r, 0);
  const avg = sum / rates.length;
  const low = rates[Math.floor(rates.length * 0.1)] ?? rates[0];
  const high = rates[Math.floor(rates.length * 0.9)] ?? rates[rates.length - 1];

  return {
    low: Math.round(low * 100) / 100,
    avg: Math.round(avg * 100) / 100,
    high: Math.round(high * 100) / 100,
    count: loads.length,
  };
}

// ─── Main Quote Engine ──────────────────────────────────────────────

export async function generateQuote(params: QuoteRequest): Promise<QuoteResponse> {
  const {
    originState,
    destState,
    equipmentType,
    weight,
    pickupDate,
    isHazmat = false,
    isTempControlled = false,
    customerId,
  } = params;

  const factors: string[] = [];
  const normalizedEquip = equipmentType.toUpperCase().replace(/[\s-]/g, "_");

  // Check for contract rate first — takes priority over spot pricing
  if (customerId) {
    const contractRate = await checkContractRate(originState, destState, normalizedEquip, customerId);
    if (contractRate) {
      const estimatedMiles = estimateDistance(originState, destState);
      const ratePerMile = contractRate.rate;
      const linehaul = contractRate.flatRate
        ? contractRate.flatRate
        : Math.round(ratePerMile * estimatedMiles * 100) / 100;
      const fuelSurcharge = Math.round(contractRate.fuelSurcharge * estimatedMiles * 100) / 100;
      const total = Math.round((linehaul + fuelSurcharge) * 100) / 100;
      const totalRatePerMile = Math.round((total / estimatedMiles) * 100) / 100;

      const timestamp = Date.now();
      const random4 = Math.random().toString(36).substring(2, 6).toUpperCase();
      const quoteId = `QT-CTR-${timestamp}-${random4}`;
      const validUntil = new Date();
      validUntil.setHours(validUntil.getHours() + QUOTE_VALIDITY_HOURS);

      return {
        quotedRate: total,
        ratePerMile: totalRatePerMile,
        estimatedMiles,
        confidence: "HIGH" as const,
        marketRate: null,
        margin: 0, // Contract rate already includes margin
        breakdown: {
          linehaul,
          fuelSurcharge,
          accessorials: 0,
          total,
        },
        validUntil: validUntil.toISOString(),
        quoteId,
        autoGenerated: true,
        factors: [
          `Contract rate applied for ${contractRate.customer.name}`,
          `Rate: $${ratePerMile.toFixed(2)}/mile${contractRate.flatRate ? ` (flat: $${contractRate.flatRate})` : ""}`,
          `Fuel surcharge: $${contractRate.fuelSurcharge.toFixed(2)}/mile`,
          `Contract valid until ${contractRate.expirationDate.toISOString().slice(0, 10)}`,
        ],
      };
    }
  }

  // 0. Try to get AI-enriched lane context (optional — failures are non-blocking)
  let laneContext: Awaited<ReturnType<typeof buildLaneContext>> | null = null;
  try {
    laneContext = await buildLaneContext(originState, destState);
  } catch {
    /* context enrichment is optional */
  }

  // 1. Estimate distance
  const estimatedMiles = estimateDistance(originState, destState);
  factors.push(`Estimated distance: ${estimatedMiles} miles (${originState.toUpperCase()} → ${destState.toUpperCase()})`);

  // 2. Get historical market data
  const market = await getMarketBenchmark(originState, destState, normalizedEquip);
  let historicalCount = 0;

  // 3. Determine base carrier rate per mile
  let carrierRatePerMile: number;

  if (market && market.count >= 3) {
    // Use historical data — derive rate per mile from average
    const avgHistoricalMiles = estimatedMiles; // Use our estimate since DB may have nulls
    carrierRatePerMile = market.avg / avgHistoricalMiles;
    historicalCount = market.count;
    factors.push(`Historical data: ${market.count} loads on this lane (last 90 days)`);
    factors.push(`Historical avg rate: $${market.avg.toFixed(2)} ($${carrierRatePerMile.toFixed(2)}/mile)`);
  } else if (laneContext?.avgRate) {
    // Use AI context engine lane intelligence as fallback before base rates
    carrierRatePerMile = laneContext.avgRate / estimatedMiles;
    factors.push(`AI lane intelligence avg rate: $${laneContext.avgRate.toFixed(2)} ($${carrierRatePerMile.toFixed(2)}/mile)`);
    if (laneContext.trend) {
      factors.push(`Lane trend: ${laneContext.trend}`);
    }
  } else {
    // Use base rates
    carrierRatePerMile = BASE_RATES[normalizedEquip] || BASE_RATES.DRY_VAN;
    factors.push(`No significant historical data — using base rate: $${carrierRatePerMile.toFixed(2)}/mile for ${normalizedEquip}`);
    if (market && market.count > 0) {
      historicalCount = market.count;
      factors.push(`Limited historical data: ${market.count} load(s) found`);
    }
  }

  // Enrich with AI context signals if available
  if (laneContext) {
    if (laneContext.seasonalFactor && laneContext.seasonalFactor !== 1.0) {
      factors.push(`AI seasonal factor: ${laneContext.seasonalFactor.toFixed(2)}`);
    }
    if (laneContext.recentAnomalies > 0) {
      factors.push(`AI anomaly alert: ${laneContext.recentAnomalies} recent anomaly(ies) on this lane`);
    }
  }

  // 4. Apply adjustments
  let adjustmentMultiplier = 1.0;

  // Hazmat adjustment
  if (isHazmat) {
    adjustmentMultiplier *= 1.15;
    factors.push("Hazmat surcharge: +15%");
  }

  // Temperature controlled
  if (isTempControlled) {
    adjustmentMultiplier *= 1.10;
    factors.push("Temperature controlled surcharge: +10%");
  }

  // Weight adjustment
  if (weight && weight > 40000) {
    adjustmentMultiplier *= 1.05;
    factors.push(`Heavyweight surcharge (${weight.toLocaleString()} lbs): +5%`);
  }

  // Seasonal adjustment
  const evalDate = pickupDate ? new Date(pickupDate) : new Date();
  const month = evalDate.getMonth() + 1; // 1-12
  if ((month >= 6 && month <= 9) || (month >= 11 && month <= 12)) {
    adjustmentMultiplier *= 1.08;
    const seasonName = month >= 11 ? "holiday" : "summer";
    factors.push(`Peak season (${seasonName}) surcharge: +8%`);
  }

  // Short haul adjustment
  if (estimatedMiles < 200) {
    adjustmentMultiplier *= 1.20;
    factors.push(`Short haul adjustment (${estimatedMiles} miles < 200): +20%/mile`);
  }

  // Long haul discount
  if (estimatedMiles > 1500) {
    adjustmentMultiplier *= 0.95;
    factors.push(`Long haul discount (${estimatedMiles} miles > 1500): -5%/mile`);
  }

  // 5. Calculate final rates
  const adjustedCarrierRatePerMile = carrierRatePerMile * adjustmentMultiplier;
  const linehaul = Math.round(adjustedCarrierRatePerMile * estimatedMiles * 100) / 100;
  const fuelSurcharge = Math.round(FUEL_SURCHARGE_PER_MILE * estimatedMiles * 100) / 100;

  // Accessorials: hazmat flat fee + temp monitoring
  let accessorials = 0;
  if (isHazmat) accessorials += 150;
  if (isTempControlled) accessorials += 100;

  const carrierTotal = linehaul + fuelSurcharge + accessorials;

  // Apply SRL margin
  const marginAmount = Math.round(carrierTotal * SRL_MARGIN * 100) / 100;
  const total = Math.round((carrierTotal + marginAmount) * 100) / 100;
  const ratePerMile = Math.round((total / estimatedMiles) * 100) / 100;

  factors.push(`SRL margin: ${(SRL_MARGIN * 100).toFixed(0)}% ($${marginAmount.toFixed(2)})`);

  // 6. Confidence scoring
  let confidence: "HIGH" | "MEDIUM" | "LOW";
  if (historicalCount >= 10) {
    confidence = "HIGH";
  } else if (historicalCount >= 3) {
    confidence = "MEDIUM";
  } else {
    confidence = "LOW";
  }

  // 7. Generate quote ID and validity
  const timestamp = Date.now();
  const random4 = Math.random().toString(36).substring(2, 6).toUpperCase();
  const quoteId = `QT-${timestamp}-${random4}`;

  const validUntil = new Date();
  validUntil.setHours(validUntil.getHours() + QUOTE_VALIDITY_HOURS);

  // 8. Market rate for response (as customer-facing rates with margin)
  let marketRate: { low: number; avg: number; high: number } | null = null;
  if (market) {
    marketRate = {
      low: Math.round(market.low * (1 + SRL_MARGIN) * 100) / 100,
      avg: Math.round(market.avg * (1 + SRL_MARGIN) * 100) / 100,
      high: Math.round(market.high * (1 + SRL_MARGIN) * 100) / 100,
    };
  }

  return {
    quotedRate: total,
    ratePerMile,
    estimatedMiles,
    confidence,
    marketRate,
    margin: SRL_MARGIN * 100,
    breakdown: {
      linehaul: Math.round((linehaul * (1 + SRL_MARGIN)) * 100) / 100,
      fuelSurcharge: Math.round((fuelSurcharge * (1 + SRL_MARGIN)) * 100) / 100,
      accessorials: Math.round((accessorials * (1 + SRL_MARGIN)) * 100) / 100,
      total,
    },
    validUntil: validUntil.toISOString(),
    quoteId,
    autoGenerated: true,
    factors,
  };
}
